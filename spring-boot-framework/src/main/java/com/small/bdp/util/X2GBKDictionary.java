	public String getHelpCode(String value) {
		return _getHelpCode(value, false);
	}

	public String getFullHelpCode(String value) {
		return _getHelpCode(value, true);
	}

	private String _getHelpCode(String value, boolean isFullCode) {
		if (StringUtils.isEmptyString(value)) {
			return "";
		}

		StringBuffer helpCode = new StringBuffer();
		//数组特殊处理
		String shorts = "";
		String longs = "";
		Iterator<String> i = _dictArrayTable.keySet().iterator();  
		while(i.hasNext()){  
			Object o = i.next();  
			String key = o.toString();
			if (value.indexOf(key) > -1){
				String aa = _dictArrayTable.get(key);
				String[] bb = aa.split("_");
				for(String c : bb){
					shorts = shorts + c.substring(0, 1);
					longs = longs + c;
				}
				value = value.replace(key, ",");
			}
		}  
		
		char[] chs = value.toCharArray();
		for (char ch : chs) {
			String s = _dictTable.get(String.valueOf(ch));
			if (s != null && s.length() > 0) {
				if (!isFullCode) {
					s = s.substring(0, 1);
				}
			} else {
				// 从
				s = String2Alpha(String.valueOf(ch));
			}

			if (!StringUtils.isEmptyString(s, true)) {
				helpCode.append(s);
			}
		}
		String rs = helpCode.toString().toLowerCase();
		if (!isFullCode) {
			rs = rs.replace(",", shorts);
		}else {
			rs = rs.replace(",", longs);
		}
		
		return rs;
	}
	
